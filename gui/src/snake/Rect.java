package snake;

public class Rect {
	private int x, y, width, height;
	private int snakeNum;
	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	public int getSnakeNum() {
		return snakeNum;
	}
	
	public void setX(int x) {
		this.x = x;
	}

	public void setY(int x) {
		this.y = y;
	}
	
}
